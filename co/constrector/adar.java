package co.constrector;

public class adar
{
	private int aid;
	private String anum;
	private String aadre;
	
	public adar()
	{
		
	}
	public adar(int aid,String anum,String aadre)
	{
		this.aid=aid;
		this.anum=anum;
		this.aadre=aadre;	
	}
	
	public int getaid()
	{
		return aid;
	}
	public String getanum()
	{
		return anum;
	}
	public String getaadre()
	{
		return aadre;
	}
}
